#include "edificio.h"


// ------------------------------------------------------------------------------------------------------------


Edificio::Edificio(){

    this -> identificador = ' ';
    this -> creador = ' ';

}


// ------------------------------------------------------------------------------------------------------------


Edificio::Edificio(char identificador, char creador){

    this -> identificador = identificador;
    this -> creador = creador;

}


// ------------------------------------------------------------------------------------------------------------


char Edificio::obtener_identificador(){
    return this -> identificador;
}


